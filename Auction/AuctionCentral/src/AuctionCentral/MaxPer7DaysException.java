package AuctionCentral;

public class MaxPer7DaysException extends Exception {

	public MaxPer7DaysException() {
		super();
	}

	public MaxPer7DaysException(String arg0) {
		super(arg0);
	}

	public MaxPer7DaysException(Throwable arg0) {
		super(arg0);
	}

	public MaxPer7DaysException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MaxPer7DaysException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
