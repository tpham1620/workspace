package AuctionCentral;

public class MoreThan90Exception extends Exception {

	public MoreThan90Exception() {
		super();
	}

	public MoreThan90Exception(String arg0) {
		super(arg0);
	}

	public MoreThan90Exception(Throwable arg0) {
		super(arg0);
	}

	public MoreThan90Exception(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MoreThan90Exception(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
